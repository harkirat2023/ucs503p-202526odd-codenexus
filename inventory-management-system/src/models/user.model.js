// src/models/user.model.js
// User Schema: username, password (hashed), role (admin/user)
const mongoose = require("mongoose");

const userSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
      trim: true,
    },
    password: {
      type: String,
      required: true,
    },
    role: {
      type: String,
      enum: ["admin", "user"],
      default: "user",
    },
  },
  {timestamps: true}
);

module.exports = mongoose.model("User", userSchema);
